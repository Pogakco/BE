const socketErrorHandler = (handler) => {
  const handleError = (error) => {
    console.error(error);
  };

  return (...args) => {
    try {
      const ret = handler.apply(this, args);
      if (ret && typeof ret.catch === "function") {
        // async handler
        ret.catch(handleError);
      }
    } catch (e) {
      // sync handler
      handleError(e);
    }
  };
};

export default socketErrorHandler;
